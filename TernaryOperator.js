// Ternary Operator
//  Condition ? expr1 : expr2

// Regular JavaScript
let  age = 19;
if(age >= 18) {
    console.log("You are an adult!");
} else {
    console.log("You are kid");
};
// Result: You are an adult!

// Ternary Operator
console.log((age >= 18) ? "You are an adult!" : "You are a kid.");
// You are an adult!
